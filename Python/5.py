# wap to find the smallest and greatest number in the list.

# Input a list of numbers (you can modify this list)
numbers = [5, 10, 2, 8, 15, 1, 20]

# Find the smallest and greatest numbers in the list
smallest = min(numbers)
greatest = max(numbers)

# Display the results
print(f"The smallest number in the list is: {smallest}")
print(f"The greatest number in the list is: {greatest}")
